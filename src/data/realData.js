// ─── UTILITY HELPERS ──────────────────────────────────────────────────────────

export const getClubById = (id) =>
  CLUBS.find((c) => c.id === id || c.id === parseInt(id));

export const getClubByName = (name) =>
  CLUBS.find((c) => c.name === name);

export const getProjectsByClub = (clubId) => {
  const club = getClubById(clubId);
  return club ? club.projects : [];
};

export const getMeetingsByClub = (clubId) => {
  const club = getClubById(clubId);
  return club ? club.meetingRecords : [];
};

export const getBodByClub = (clubId) => {
  const club = getClubById(clubId);
  return club ? club.bod : [];
};

export const getDuesByClub = (clubId) =>
  DUES_RECORDS.filter((d) => d.clubId === clubId);

export const getMonthlyByClub = (clubId) => {
  const club = getClubById(clubId);
  return club ? club.monthly : [];
};
